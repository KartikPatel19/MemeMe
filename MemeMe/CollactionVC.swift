import UIKit

private let reuseIdentifier = "Cell"

class CollactionVC: UICollectionViewController,UICollectionViewDelegateFlowLayout {

    var memes = [Meme]()
    
    override func viewDidLoad() {
        super.viewDidLoad()
        self.collectionView!.register(UICollectionViewCell.self, forCellWithReuseIdentifier: reuseIdentifier)
    }
    
    override func viewWillAppear(_ animated: Bool) {
        super.viewWillAppear(animated)
        let appDelegate = UIApplication.shared.delegate as! AppDelegate
        memes = appDelegate.memes
    }

    override func numberOfSections(in collectionView: UICollectionView) -> Int {
        // #warning Incomplete implementation, return the number of sections
        return memes.count
    }


    override func collectionView(_ collectionView: UICollectionView, cellForItemAt indexPath: IndexPath) -> UICollectionViewCell {
        let cell = collectionView.dequeueReusableCell(withReuseIdentifier: "VillainCollectionViewCell", for: indexPath)
    
        // Configure the cell
        let cellImageView = cell.viewWithTag(1) as! UIImageView
        cellImageView.image = memes[indexPath.row].memedImage
        
        return cell
    }

